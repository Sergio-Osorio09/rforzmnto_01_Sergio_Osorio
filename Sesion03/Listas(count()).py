"""
Listas count(): cantidad de veces que aparece un elemento que se repite en una lista
"""

list_1 = ["python", "python", "java", "PHP", "Ruby", "JavaScript", "Typescript", "java", "java", "java"]

print("Mi lista tiene los siguientes valores: {}".format(list_1))
print("la cantidad de veces que aparece python es : {}".format(list_1.count("python")))
print("la cantidad de veces que aparece java es : {}".format(list_1.count("java")))


